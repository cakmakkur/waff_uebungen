package at.technikum.week1;

public class EscapeCharacters {
    static {
        System.out.println("Escape Characters");
        System.out.println("escape sequence is started with a backslash: \\");
        System.out.println("to print a tab ( ) use \"\\t\"");
        System.out.println("to print a backslash (\\) use \"\\\\\"");
        System.out.println("to print double quotes (\") use \"\\\"\"");
        System.out.println("to print single quotes (\') use \"\\\'\"");
        System.out.println("to start a new line (");
        System.out.println(") use \"\\n\"");
    }
}